     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            $session = new Session();
            $session->session = $request->input('session');
            $session->status =  1;
            $session->save();

            return response()->json([
                'status' => 200,
                'message' => 'Session addes succesfully!',
            ]);
        } catch (\Throwable $th) {
            return response()->json([
                'status' => 403,
                'message' => $th,
            ]);
        }
    }

